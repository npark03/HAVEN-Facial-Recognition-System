    if image is None:
        print(f"[WARN] Could not read {image_path}")
        continue

    face_box = detect_largest_face(image)
    if face_box is None:
        print(f"[WARN] No face found in {image_path}")
        continue

    try:
        face = crop_face(image, face_box)
        emb = get_embedding(face)
        nathan_embeddings.append(emb.tolist())
    except Exception as e:
        print(f"[WARN] Failed on {image_path}: {e}")

