def resolve_to_session_file(image_path_str):
    filename = Path(image_path_str).name
    return SESSION_DIR / filename

def predict_authorized(image_path):
    image = cv2.imread(str(image_path))
    if image is None:
        return {
            "authorized": False,
            "identity": None,
            "distance": None,
            "reason": "could_not_read_image"
        }

    face_box = detect_largest_face(image)
    if face_box is None:
        return {
            "authorized": False,
            "identity": None,
            "distance": None,
            "reason": "no_face_detected"
        }

    try:
        face = crop_face(image, face_box)
        query_embedding = get_embedding(face)
        identity, distance = compare_embedding(query_embedding, known_embeddings)

        authorized = distance < THRESHOLD

        return {
            "authorized": bool(authorized),
            "identity": identity if authorized else "unknown",
            "closest_match": identity,
            "distance": round(distance, 4),
            "threshold": THRESHOLD,
            "reason": "ok" if authorized else "face_not_recognized"
        }

    except Exception as e:
        return {
            "authorized": False,
            "identity": None,
            "distance": None,
            "reason": f"error: {e}"
        }

