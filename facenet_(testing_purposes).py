embedder = FaceNet()

CASCADE_PATH = cv2.data.haarcascades + "haarcascade_frontalface_default.xml"
face_detector = cv2.CascadeClassifier(CASCADE_PATH)

if face_detector.empty():
    raise RuntimeError(f"Could not load Haar cascade: {CASCADE_PATH}")

def detect_largest_face(image_bgr):
    gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)

    faces = face_detector.detectMultiScale(
        gray,
        scaleFactor=1.1,
        minNeighbors=5,
        minSize=(80, 80)
    )

    if len(faces) == 0:
        return None

    faces = sorted(faces, key=lambda f: f[2] * f[3], reverse=True)
    return faces[0]


def crop_face(image_bgr, face_box, padding=0.20):
    x, y, w, h = face_box
    img_h, img_w = image_bgr.shape[:2]

    pad_w = int(w * padding)
    pad_h = int(h * padding)

    x1 = max(0, x - pad_w)
    y1 = max(0, y - pad_h)
    x2 = min(img_w, x + w + pad_w)
    y2 = min(img_h, y + h + pad_h)

    face = image_bgr[y1:y2, x1:x2]

    if face.size == 0:
        raise ValueError("Face crop was empty")

    return face


def get_embedding(face_bgr):
    face_rgb = cv2.cvtColor(face_bgr, cv2.COLOR_BGR2RGB)
    emb = embedder.embeddings([face_rgb])[0]
    emb = np.asarray(emb, dtype=np.float32)

    norm = np.linalg.norm(emb)
    if norm == 0:
        raise ValueError("Embedding norm was zero")

    return emb / norm


def compare_embedding(query_embedding, known_embeddings_dict):
    best_name = None
    best_distance = float("inf")

    for name, stored_embedding in known_embeddings_dict.items():
        known_embedding = np.asarray(stored_embedding, dtype=np.float32)

        # supports either one embedding or a list of embeddings
        if known_embedding.ndim == 1:
            distances = [float(np.linalg.norm(query_embedding - known_embedding))]
        else:
            distances = [
                float(np.linalg.norm(query_embedding - emb))
                for emb in known_embedding
            ]

        distance = min(distances)

        if distance < best_distance:
            best_distance = distance
            best_name = name

    return best_name, best_distance

