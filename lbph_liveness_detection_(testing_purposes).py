print("Image 2 shape:", img2.shape)

box1 = detect_largest_face_box(img1)
box2 = detect_largest_face_box(img2)

if box1 is None:
    raise RuntimeError("No face detected in image 1")
if box2 is None:
    raise RuntimeError("No face detected in image 2")

img1_boxed = draw_face_box(img1, box1)
img2_boxed = draw_face_box(img2, box2)

show_image("Step 3: Face Detection on Image 1", img1_boxed)
show_image("Step 4: Face Detection on Image 2", img2_boxed)

print("Face box 1:", box1)
print("Face box 2:", box2)

center1 = face_center(box1)
center2 = face_center(box2)

img1_centered = draw_center(img1_boxed, center1)
img2_centered = draw_center(img2_boxed, center2)

show_image("Step 5: Face Center in Image 1", img1_centered)
show_image("Step 6: Face Center in Image 2", img2_centered)

print("Face center 1:", center1)
print("Face center 2:", center2)

dx = center2[0] - center1[0]
dy = center2[1] - center1[1]

print("Step 7: Movement Calculation")
print("movement_x =", round(dx, 2))
print("movement_y =", round(dy, 2))

if challenge == "LOOK_LEFT":
    passed = dx <= -HORIZONTAL_THRESHOLD
elif challenge == "LOOK_RIGHT":
    passed = dx >= HORIZONTAL_THRESHOLD
elif challenge == "LOOK_UP":
    passed = dy <= -VERTICAL_THRESHOLD
else:
    passed = False

print("Step 8: Liveness Decision")
print("Challenge:", challenge_text)
print("Horizontal threshold:", HORIZONTAL_THRESHOLD)
print("Vertical threshold:", VERTICAL_THRESHOLD)
print("Liveness Passed:", passed)

