CASCADE_PATH = cv2.data.haarcascades + "haarcascade_frontalface_default.xml"

# Liveness thresholds
HORIZONTAL_THRESHOLD = 18
VERTICAL_THRESHOLD = 10

with open(METADATA_FILE, "r") as f:
    metadata = json.load(f)

face_detector = cv2.CascadeClassifier(CASCADE_PATH)

if face_detector.empty():
    raise RuntimeError(f"Could not load Haar cascade: {CASCADE_PATH}")

def show_image(title, image_bgr, figsize=(6,6)):
    image_rgb = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
    plt.figure(figsize=figsize)
    plt.imshow(image_rgb)
    plt.title(title)
    plt.axis("off")
    plt.show()


def detect_largest_face_box(image_bgr):
    gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)

    faces = face_detector.detectMultiScale(
        gray,
        scaleFactor=1.1,
        minNeighbors=5,
        minSize=(80, 80)
    )

    if len(faces) == 0:
        return None

    faces = sorted(faces, key=lambda f: f[2] * f[3], reverse=True)
    return faces[0]


def draw_face_box(image_bgr, face_box, color=(0, 255, 0)):
    boxed = image_bgr.copy()
    x, y, w, h = face_box
    cv2.rectangle(boxed, (x, y), (x + w, y + h), color, 3)
    return boxed


def face_center(face_box):
    x, y, w, h = face_box
    return (x + w / 2.0, y + h / 2.0)


def draw_center(image_bgr, center, color=(255, 0, 0)):
    marked = image_bgr.copy()
    cx, cy = int(center[0]), int(center[1])
    cv2.circle(marked, (cx, cy), 8, color, -1)
    return marked

round_21_info = None

