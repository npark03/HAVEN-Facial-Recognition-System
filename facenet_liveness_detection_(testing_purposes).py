def load_image_rgb(path):
    img_bgr = cv2.imread(path)
    if img_bgr is None:
        return None
    return cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

def get_landmarks(image_rgb):
    results = face_mesh.process(image_rgb)
    if not results.multi_face_landmarks:
        return None

    face_landmarks = results.multi_face_landmarks[0]
    h, w, _ = image_rgb.shape

    pts = []
    for lm in face_landmarks.landmark:
        pts.append((lm.x * w, lm.y * h))
    return np.array(pts, dtype=np.float32)

def safe_mean(points):
    return np.mean(points, axis=0)

def estimate_pose_features(landmarks):
    """
    Lightweight face-pose approximation from facial landmarks.
    Returns yaw and pitch style measures using relative landmark geometry.

    Landmark groups:
      left eye outer area
      right eye outer area
      nose tip
      chin
      forehead-ish
    """

    # Mediapipe landmark indices
    LEFT_EYE = [33, 133]
    RIGHT_EYE = [362, 263]
    NOSE_TIP = 1
    CHIN = 152
    FOREHEAD = 10

    left_eye_center = safe_mean(landmarks[LEFT_EYE])
    right_eye_center = safe_mean(landmarks[RIGHT_EYE])
    nose_tip = landmarks[NOSE_TIP]
    chin = landmarks[CHIN]
    forehead = landmarks[FOREHEAD]

    eye_center = (left_eye_center + right_eye_center) / 2.0
    eye_distance = np.linalg.norm(right_eye_center - left_eye_center)

    if eye_distance < 1e-6:
        return None

    # Yaw proxy:
    # compare distance from nose to left eye vs nose to right eye
    dist_left = np.linalg.norm(nose_tip - left_eye_center)
    dist_right = np.linalg.norm(nose_tip - right_eye_center)
    yaw = (dist_left - dist_right) / eye_distance

    # Pitch proxy:
    # compare nose vertical placement between forehead and chin
    face_height = np.linalg.norm(chin - forehead)
    if face_height < 1e-6:
        return None

    pitch = ((nose_tip[1] - forehead[1]) / face_height)

    return {
        "yaw": float(yaw),
        "pitch": float(pitch),
        "eye_distance": float(eye_distance)
    }

def evaluate_liveness_challenge(challenge, pose1, pose2):
    """
    Returns pass/fail and score interpretation.
    """
    if pose1 is None or pose2 is None:
        return False, "pose_estimation_failed", None

    yaw_delta = pose2["yaw"] - pose1["yaw"]
    pitch_delta = pose2["pitch"] - pose1["pitch"]

    if challenge == "LOOK_LEFT":
        passed = yaw_delta < -YAW_THRESHOLD
        score = yaw_delta
        note = "expected more negative yaw change"
    elif challenge == "LOOK_RIGHT":
        passed = yaw_delta > YAW_THRESHOLD
        score = yaw_delta
        note = "expected more positive yaw change"
    elif challenge == "LOOK_UP":
        # when looking up, nose tends to move upward relative to face shape,
        # so pitch proxy typically decreases
        passed = pitch_delta < -PITCH_UP_THRESHOLD
        score = pitch_delta
        note = "expected more negative pitch change"
    else:
        passed = False
        score = None
        note = "unknown challenge"

    return passed, note, {
        "yaw_delta": float(yaw_delta),
        "pitch_delta": float(pitch_delta),
        "raw_score": float(score) if score is not None else None
    }

