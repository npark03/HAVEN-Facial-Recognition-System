def detect_largest_face_box(image_bgr):
    gray = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2GRAY)

    faces = face_detector.detectMultiScale(
        gray,
        scaleFactor=1.1,
        minNeighbors=5,
        minSize=(80, 80)
    )

    if len(faces) == 0:
        return None

    faces = sorted(faces, key=lambda f: f[2] * f[3], reverse=True)
    return faces[0]


def face_center(face_box):
    x, y, w, h = face_box
    return (x + w / 2.0, y + h / 2.0)


def check_liveness(challenge, box1, box2):
    c1 = face_center(box1)
    c2 = face_center(box2)

    dx = c2[0] - c1[0]
    dy = c2[1] - c1[1]

    if challenge == "LOOK_LEFT":
        passed = dx <= -HORIZONTAL_THRESHOLD
    elif challenge == "LOOK_RIGHT":
        passed = dx >= HORIZONTAL_THRESHOLD
    elif challenge == "LOOK_UP":
        passed = dy <= -VERTICAL_THRESHOLD
    else:
        passed = False

    debug = {
        "movement_x": round(float(dx), 2),
        "movement_y": round(float(dy), 2),
        "face1_center": [round(float(c1[0]), 2), round(float(c1[1]), 2)],
        "face2_center": [round(float(c2[0]), 2), round(float(c2[1]), 2)],
    }

    return passed, debug


def crop_face(gray_image, face_box, padding=0.15):
    x, y, w, h = face_box
    img_h, img_w = gray_image.shape[:2]

    pad_w = int(w * padding)
    pad_h = int(h * padding)

    x1 = max(0, x - pad_w)
    y1 = max(0, y - pad_h)
    x2 = min(img_w, x + w + pad_w)
    y2 = min(img_h, y + h + pad_h)

    face = gray_image[y1:y2, x1:x2]
    if face.size == 0:
        raise ValueError("Face crop was empty")

    face = cv2.resize(face, (200, 200))
    return face


